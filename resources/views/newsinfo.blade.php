            <p class="nd_content">截至今年5月末，全市银行业各项贷款余额7768亿元，较年初增加130.8亿元，同比增长2.65%，位居全省第6位，2015年前移了4位。他们持续优化信贷期限，对符合条件的企业发放固定资产贷款、技改贷款，拉长信贷期限。今年1至5月全市累计处置不良贷款112.2亿元，比去年同期多处置25.9亿元，增长30%，处置速度快于新发生速度，“风险突围”成效得到进一步巩固。逾期贷款占比6.80%，同比下降2.5个百分点，不良风险从源头上得到有效控制。并从深化企业帮扶入手，建立帮扶解困会商机制，出台银行帮扶风险企业和处置不良贷款自律公约，成功帮扶受担保链风险影响企业9114家，涉及风险敞口341亿元。同时，还建立分类帮扶机制，将企业分为重点保护类、重点帮扶类、资不抵债类和涉嫌“逃废债”等四种类型，分别予以扶持、协调、处置与打击；积极协助市政府制定银行“同进共退”帮扶企业实施意见，约束银行业发挥合力，理性帮扶</p>
